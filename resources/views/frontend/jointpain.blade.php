        <h2 class="mt-3 text-2xl font-extrabold text-gray-700">What are the symptoms of joint pain:</h2>
        <p class="text-gray-600">
            In some cases, your joint pain will require you to see a doctor. You should make an appointment if you don’t
            know the cause of your joint pain and are experiencing other unexplained symptoms.
        </p>
        <p>You should also see a doctor if:</p>
        <div style="margin-left: 50px">
            <li>The area around the joint is swollen, red, tender, or warm to the touch</li>
            <li>The pain persists for three days or more</li>
            <li>You have a fever but no other signs of the flu</li>
            <li>You’ve experienced a serious injury.</li>
            <li>The joint appears deformed.</li>
        </div>

        <h2 class="mt-3 text-2xl font-extrabold text-gray-700">Home treatment:</h2>
        <p>
            Doctors consider both OA and RA to be chronic conditions. There’s no treatment currently available that will
            completely eliminate the joint pain associated with arthritis or keep it from returning. However, there are
            ways to manage the pain:
        </p>
        <div style="margin-left: 50px">
            <li>
                It may help to use topical pain relievers or take nonsteroidal anti-inflammatory drugs to reduce pain,
                swelling, and inflammation.
            </li>
            <li>Stay physically active and follow a fitness program focusing on moderate exercise.</li>
            <li>Stretch before exercising to maintain a good range of motion in your joints.</li>
            <li>Keep your body weight within a healthy range. This will lessen stress on the joints.</li>
            <li>
                If your pain isn’t due to arthritis, you can try taking a nonprescription, anti-inflammatory drug,
                getting a massage, taking a warm bath, stretching frequently, and getting adequate rest.
            </li>
